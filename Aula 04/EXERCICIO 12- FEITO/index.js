//Área: A = π * r2
//Circunferência: C = 2 * π * r
//Considere π = 3.14 .

let raio = 7
let resultadoraio = raio * 7
let valorpi = 3.14
let resultadoarea = valorpi * resultadoraio
let resultadocircunferência = 2 * valorpi * raio

console.log(resultadoarea)
console.log(resultadocircunferência)

console.log(`Area do circulo: ${valorpi * resultadoraio}`)
