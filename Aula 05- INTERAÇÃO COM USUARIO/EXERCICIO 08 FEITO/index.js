//8. Solicite ao usuário que insira uma temperatura em graus Celsius e converta essa
//temperatura para Fahrenheit usando a fórmula

let temperatura_graus_celsius = Number(prompt('insira uma temperatura em celsius'))
let fahrenheit= (9/5) * temperatura_graus_celsius + 32

console.log(`A temperatura para fahrenheit é de = ${fahrenheit}`)
console.log('fahrenheit', fahrenheit)
